  echo -e "      .==----------::.:-=++++======++++++++::---------=+-"
  echo -e "     -+=----------:..=+++=-:::---::::-+++++-:-----------++. "
  echo -e "    ==------------:.+***=:-----------:.+***=:------------=+-"
  echo -e "   +=-------------::****-::------------:===---------------=+-"
  echo -e "  =+=============-:.*####=:..:::::--------==================+-"
  echo -e " -+===============--:=#######*++++==--::::--================++."
  echo -e ".++==================--=+**#%%%%%%%%%%%%#+-:-================+= "
  echo -e "-++=================--====-----===+++*%%%%%#:-============+++++"
  echo -e "++++++++++++++===-:-==-:-============-:-%@@@%:-=+++++++++++++++:"
  echo -e "++++++++++++++++=-:@@@@+:==++++++++++=-.*@@@@:=++++++++++++++++:"
  echo -e "+++++++++++++==+=-.@@@@@+:-=========--.-@@@@#-=++++++++++++++++:"
  echo -e "=+++++++++++:%+-=-.@@@@@@@*+-:::.::-=+%@@@@*-=++++++++++++++++*"
