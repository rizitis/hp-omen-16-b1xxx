RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[0;33m'
BLUE='\033[0;34m'
MAGENTA='\033[0;35m'
CYAN='\033[0;36m'
WHITE='\033[0;37m'
BLINK='\033[5m'
BOLD='\033[1m'
NORMAL='\033[0m'


function display_letter() {
  local letter=$1
  local color=$2
  local effect=$3

  echo -e "${effect}${color}${letter}${NORMAL}"
}


function generate_birthday_message() {
  local message="Happy 30th Birthday, Slackware Linux!"


  clear


  for ((i=0; i<${#message}; i++)); do
    local letter="${message:$i:1}"
    local color=""
    local effect=""

    case $((i % 6)) in
      0)
        color=$RED
        effect=$BLINK
        ;;
      1)
        color=$GREEN
        effect=$BOLD
        ;;
      2)
        color=$YELLOW
        effect=$BLINK
        ;;
      3)
        color=$BLUE
        effect=$BOLD
        ;;
      4)
        color=$MAGENTA
        effect=$BLINK
        ;;
      5)
        color=$CYAN
        effect=$BOLD
        ;;
    esac

    display_letter "$letter" "$color" "$effect"
    sleep 0.1
  done

